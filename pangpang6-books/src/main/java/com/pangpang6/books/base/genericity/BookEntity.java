package com.pangpang6.books.base.genericity;

import java.io.Serializable;

public class BookEntity implements Serializable {
    private int id;
    private String name;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
